const DAY_MS = 86_400_000;
const EPOCH = Date.UTC(2020, 0, 1);

function escapeHtml(text) {
  return String(text).replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;").replace(/"/g, "&quot;");
}

export function quoteIndexForDate(date = new Date()) {
  const day = Math.floor(Date.UTC(date.getFullYear(), date.getMonth(), date.getDate()) - EPOCH) / DAY_MS;
  return ((day % quotes.length) + quotes.length) % quotes.length;
}

export function pickQuote(date = new Date()) {
  return quotes[quoteIndexForDate(date)];
}

export function renderQuote(quote) {
  return `
    <blockquote class="quote">
      <p>${escapeHtml(quote.text)}</p>
      <cite>${escapeHtml(quote.author)}</cite>
    </blockquote>
  `;
}
