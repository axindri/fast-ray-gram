export function pickQuote() {
  return quotes[Math.floor(Math.random() * quotes.length)];
}

export function renderQuote(quote) {
  return `
    <blockquote class="quote">
      <p>${quote.text}</p>
      <cite>${quote.author}</cite>
    </blockquote>
  `;
}
